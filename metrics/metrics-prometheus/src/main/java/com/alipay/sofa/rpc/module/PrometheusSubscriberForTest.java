package com.alipay.sofa.rpc.module;

import com.alipay.sofa.rpc.event.Event;
import com.alipay.sofa.rpc.event.PrometheusSubscriber;

/**
 * @author zhaowang
 * @version : PrometheusSubscriberForTest.java, v 0.1 2020年04月06日 4:11 下午 zhaowang Exp $
 */
public class PrometheusSubscriberForTest extends PrometheusSubscriber {

    private Event event;

    @Override
    public void onEvent(Event event) {
        this.event = event;
    }

    public void reset() {
        event = null;
    }

    public Event getEvent() {
        return event;
    }
}